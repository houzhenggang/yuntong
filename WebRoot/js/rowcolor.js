function changecolor(curTR){
	//curTR.borderColorDark = "#4E4E4E";
	curTR.bgColor="#CCCCCC";
	//curTR.borderColorLight = "#FF3366";
	curTR.style.color = "#4E4E4E";
}

function changeback(curTR){
	//curTR.borderColorDark = "#FF3399";
	curTR.bgColor="#FFFFFF";
	//curTR.borderColorLight = "#000000";
	curTR.style.color = "#000000";
}